const debug = require('debug')('discomoka:DB/MessageLog');
const db = require('./base');

const MessageLog = {
    tableName: "message_log",
    pkName: "id",
    insert: async (message) => {
        debug('Logging message from U=%s in C=%s: %s', message.user_id, message.channel_id, message.content);
        return await db.insert(MessageLog.tableName, message);
    }
};

module.exports = MessageLog;
